package com.upreader.controller;

public class ProjectForm {
	private String title;
	private String genre;
	private String subgenres;
	private String book;
	private String sample;
	private String cover;
	private String pitch;
	private String synopsis;
	private String references;
	private String backstory;
	
	private String bookprice;
	private String pubyears;
	private String percentsale;
	
	public ProjectForm() {
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getGenre() {
		return genre;
	}

	public void setGenre(String genre) {
		this.genre = genre;
	}

	public String getSubgenres() {
		return subgenres;
	}

	public void setSubgenres(String subgenres) {
		this.subgenres = subgenres;
	}

	public String getBook() {
		return book;
	}

	public void setBook(String book) {
		this.book = book;
	}

	public String getSample() {
		return sample;
	}

	public void setSample(String sample) {
		this.sample = sample;
	}

	public String getCover() {
		return cover;
	}

	public void setCover(String cover) {
		this.cover = cover;
	}

	public String getPitch() {
		return pitch;
	}

	public void setPitch(String pitch) {
		this.pitch = pitch;
	}

	public String getSynopsis() {
		return synopsis;
	}

	public void setSynopsis(String synopsis) {
		this.synopsis = synopsis;
	}

	public String getReferences() {
		return references;
	}

	public void setReferences(String references) {
		this.references = references;
	}

	public String getBackstory() {
		return backstory;
	}

	public void setBackstory(String backstory) {
		this.backstory = backstory;
	}

	public String getBookprice() {
		return bookprice;
	}

	public void setBookprice(String bookprice) {
		this.bookprice = bookprice;
	}

	public String getPubyears() {
		return pubyears;
	}

	public void setPubyears(String pubyears) {
		this.pubyears = pubyears;
	}

	public String getPercentsale() {
		return percentsale;
	}

	public void setPercentsale(String percentsale) {
		this.percentsale = percentsale;
	}
}
